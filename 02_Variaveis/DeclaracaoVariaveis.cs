using System;

class DeclaracaoVariaveis
{
	
static void Main()
	{
		/*
		Dclaração de variavél
		*/
		
		// Inicializando da Variável
		int a;
		a=22;
		Console.WriteLine(a);
		
		//Reatribuição de valor na variável
		a = 56;
		Console.WriteLine(a);
		
		//Outras formas de declaração de variáveis
		
